package com.najdi.android.najdiapp.home.model;

public class AttributeOptionModel {
    private String id;
    private String option_name;
    private String option_price;
    private String price_visibility;


    public String getPriceVisibility() {
        return price_visibility;
    }

    public String getOptionName() {
        return option_name;
    }

    public String getOptionPrice() {
        return option_price;
    }

    public String getId() {
        return id;
    }
}
